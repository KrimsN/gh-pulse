-- 001_events.sql — сырые события GitHub, центральная таблица проекта.
-- Forward-only: миграции не откатываются, изменение едет следующим номером.
--
-- Форма колонок выведена из реального часа GH Archive (2026-06-01-15, 149 565 событий), а не из
-- спецификации API. Числа в комментариях ниже — оттуда же. Особенности данных и две эпохи формата
-- описаны в docs/adr/0007-hybrid-data-epochs.md.

CREATE DATABASE IF NOT EXISTS ghpulse;

CREATE TABLE IF NOT EXISTS ghpulse.events
(
    -- GitHub отдаёт .id строкой ("12660541189"), но это целое: коллектор приводит к UInt64.
    -- UInt32 не подходит — измеренный максимум 1.27e10 уже втрое выше его потолка (4.29e9).
    event_id     UInt64,

    -- 15 уникальных значений на весь поток: словарь LowCardinality здесь идеален.
    event_type   LowCardinality(String),

    -- Delta + ZSTD: строки лежат отсортированными по created_at (вторая колонка ORDER BY),
    -- поэтому соседние значения различаются на секунды — Delta превращает их в маленькие числа,
    -- которые ZSTD ужимает почти в ничто. На неотсортированной колонке приёма не было бы.
    created_at   DateTime CODEC(Delta, ZSTD),

    -- ID укладываются в UInt32 (максимумы 2.9e8 и 1.26e9), но repo_id уже израсходовал 29%
    -- его диапазона и продолжает расти. Переполнение здесь — это молча испорченные данные,
    -- а ORDER BY потом не переделать; 4 лишних байта на колонку дешевле такого риска.
    actor_id     UInt64,

    -- Вопреки первоначальной схеме — НЕ LowCardinality: 65 971 уникальный логин за один час,
    -- на корпусе счёт пойдёт на миллионы. LowCardinality окупается примерно до 10 000 значений,
    -- дальше словарь сам становится обузой.
    actor_login  String,

    repo_id      UInt64,

    -- Всегда в форме owner/name (149 565 из 149 565). Кардинальность высокая — 76 990 за час,
    -- поэтому обычный String.
    repo_name    String,

    -- 0 = событие вне организации. org приходит лишь у 14% событий, и Nullable означал бы
    -- null-маску на весь корпус ради признака, который однозначно кодируется нулём:
    -- организации с id = 0 в GitHub не существует.
    org_id       UInt64 DEFAULT 0,

    -- В событии языка нет вообще — ни сверху, ни в payload (даже forkee.language приходит null).
    -- Заполняется отдельным обогащением; до него пустая строка. Языков сотни — LowCardinality.
    language     LowCardinality(String),

    -- Длина payload в байтах. Осторожно: величина несопоставима между эпохами данных (ADR 0007) —
    -- у PushEvent новой эпохи это фактически константа (медиана 176 байт), у WatchEvent всегда 20.
    -- Годится как грубый признак внутри одной эпохи, но не как метрика активности.
    payload_size UInt32,

    -- .payload.ref есть только у Push/Create/DeleteEvent, у остальных типов пустая строка.
    ref          String
)
ENGINE = MergeTree
-- Партиция на месяц. Корпус (бэкфилл 2024-2025 + текущий поток) даёт порядка 30 партиций —
-- намного ниже той сотни-тысячи, после которой ClickHouse начинает страдать от их числа.
-- Помесячная нарезка нужна для управления данными (долив, DROP PARTITION при перезаливке),
-- а отсечение по времени в запросах делает ORDER BY.
PARTITION BY toYYYYMM(created_at)
-- Порядок продиктован тем, как таблицу читают, и правилом «сначала низкая кардинальность»:
--   event_type  — 15 значений: индекс сразу отсекает гранулы чужих типов. Основной запрос проекта
--                 («топ репозиториев по звёздам») фильтрует WHERE event_type = 'WatchEvent';
--   created_at  — окно времени есть в каждом аналитическом запросе, внутри типа даёт range-скан;
--   repo_id     — группировка идёт по нему, а соседство строк одного репозитория внутри секунды
--                 улучшает сжатие.
ORDER BY (event_type, created_at, repo_id)
-- TTL намеренно нет. Корпус — исторический архив, его ценность в глубине: политика «удалять
-- старше N лет» стёрла бы бэкфилл 2024-2025, ради которого он и грузится (ADR 0007), причём молча,
-- фоновым мержем. Данные не растут сами по себе — сколько залито, столько и лежит; чистка при
-- необходимости делается явно, через DROP PARTITION.
SETTINGS index_granularity = 8192;
