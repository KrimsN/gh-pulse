-- 002_mv_hourly.sql — почасовые/дневные агрегаты поверх ghpulse.events.
-- Forward-only, продолжает 001_events.sql.
--
-- ВАЖНО (проверено вручную на живом инстансе перед этой миграцией): materialized view в ClickHouse —
-- это триггер на INSERT, а не отложенный view. Он видит только строки, вставленные ПОСЛЕ CREATE.
-- На 14 052 941 существующих строках `ghpulse.events` тестовая MV с идентичным определением сразу
-- после создания дала count()=0, sum(stars)=0 — бэкфилл существующих данных сюда не входит,
-- это отдельная задача 2.2 (`INSERT INTO <mv> SELECT … FROM events WHERE …`).
--
-- Все три MV — SummingMergeTree: на вставке ClickHouse суммирует численные колонки, не входящие
-- в ORDER BY, в рамках уже смерженных кусков; несмерженные куски могут содержать несколько строк
-- с одинаковым ключом. Поэтому SELECT из любой из них обязан агрегировать на чтении
-- (`GROUP BY … SUM(...)`), а не полагаться на то, что строка уже одна — это касается кода будущих
-- эндпоинтов (2.3, 2.4), не самой миграции.

-- ─────────────────────────────────────────────────────────────────────────────────────────────
-- 1) repo_stars_hourly_mv — звёзды по репозиторию по часам.
--
-- Источник для двух разных паттернов чтения:
--   а) уже существующий /api/v1/trending (1.8/1.9, будет переведён на эту MV в 2.3) — фильтр
--      ТОЛЬКО по времени (WHERE hour >= …), без equality по repo_id, читает многие репозитории сразу;
--   б) будущий /api/v1/repos/{owner}/{name} (2.4) — equality WHERE repo_id = X, ORDER BY hour для
--      тайм-серии одного репозитория, диапазон по времени вторичен.
--
-- ORDER BY (repo_id, hour), а не (hour, repo_id): это компромисс в пользу паттерна (б), а не (а).
-- Причина — цена промаха у каждого паттерна разная:
--   - с ORDER BY (repo_id, hour) паттерн (б) получает честный range-скан по первичному индексу
--     (equality на repo_id сразу сужает до гранул одного репозитория, дальше hour уже отсортирован);
--     паттерн (а) без фильтра по repo_id вообще не использует индекс избирательно — читает все
--     гранулы партиции целиком в любом случае, независимо от того, какая колонка идёт первой;
--   - с ORDER BY (hour, repo_id) — паттерн (а) выиграл бы только PARTITION BY-отсечением (уже есть
--     от PARTITION BY toYYYYMM(hour) и не зависит от ORDER BY), а паттерн (б) деградировал бы до
--     полного скана партиции на каждый repo_id, потому что repo_id стал бы неселективным вторым
--     полем без ведущего условия на hour в запросе.
-- Итог: (а) не проигрывает от выбора порядка (спасает PARTITION BY), а (б) проигрывает сильно без
-- repo_id первым — поэтому repo_id ведёт.
--
-- `hour` — второй столбец ORDER BY, локально монотонен внутри каждого repo_id (та же логика, что и
-- `created_at` в 001_events.sql, только там монотонность была внутри event_type) — Delta+ZSTD оправдан
-- тем же способом.
CREATE MATERIALIZED VIEW IF NOT EXISTS ghpulse.repo_stars_hourly_mv
(
    repo_id   UInt64,
    repo_name String,
    hour      DateTime CODEC(Delta, ZSTD),
    stars     UInt64
)
ENGINE = SummingMergeTree
PARTITION BY toYYYYMM(hour)
ORDER BY (repo_id, hour)
AS
SELECT
    repo_id,
    any(repo_name) AS repo_name,
    toStartOfHour(created_at) AS hour,
    count() AS stars
FROM ghpulse.events
WHERE event_type = 'WatchEvent'
GROUP BY repo_id, hour;

-- ─────────────────────────────────────────────────────────────────────────────────────────────
-- 2) language_daily_mv — события по языку по дням.
--
-- Источник для будущего GET /api/v1/languages/trends?window=30d&granularity=day (2.4) — контракт
-- гарантирует временное окно на каждый запрос (`window`), equality по конкретному языку в
-- опубликованном контракте (docs/ARCHITECTURE.md) не заявлена, но по аналогии с опциональным
-- `language` у /trending не исключена в будущем.
--
-- WHERE language != '': измерено на живых данных перед написанием этой миграции —
-- countIf(language != '') = 0 из 14 052 941 строк (0%, обогащение — задача 4.3, ещё не запускалось).
-- Без фильтра единственным "языком" в агрегате была бы пустая строка на 100% строк, что обесценивает
-- сам смысл MV до его наполнения обогащёнными данными; будущий эндпоинт 2.4 отдельно считает и
-- честно показывает `coverage` (долю событий с известным языком) по сырой `events`, а не по этой MV —
-- MV намеренно узкая: только то, что уже можно строить в разрезе языка.
--
-- ORDER BY (day, language): `day` ведёт, потому что это единственный фильтр, гарантированно
-- присутствующий в каждом запросе контракта (`window` обязателен), а `language` — опциональный,
-- как `language` у /trending. День даёт range-скан по первичному индексу на нужное окно; отбор по
-- конкретному языку внутри уже отобранных гранул дёшев — после агрегации по (день, язык) на день
-- приходится не более нескольких сотен строк (по числу различных языков), а не миллионы сырых
-- событий. Обратный порядок (language, day) ускорил бы гипотетический будущий фильтр по языку, но
-- сделал бы обязательный `window` неселективным индексом (пришлось бы сканировать гранулы всех
-- языков, чтобы найти нужный диапазон дат внутри каждого) — то есть штраф лёг бы на запрос, который
-- выполняется всегда, ради запроса, которого в контракте пока нет.
--
-- `day` — первый столбец ORDER BY и потому строго монотонен по всей таблице целиком (сильнее, чем
-- `created_at` в 001_events.sql, где монотонность только локальная — в пределах event_type,
-- второго столбца, а не первого) — кандидат на Delta+ZSTD ещё увереннее, чем там.
CREATE MATERIALIZED VIEW IF NOT EXISTS ghpulse.language_daily_mv
(
    day      Date CODEC(Delta, ZSTD),
    language LowCardinality(String),
    events   UInt64
)
ENGINE = SummingMergeTree
PARTITION BY toYYYYMM(day)
ORDER BY (day, language)
AS
SELECT
    toDate(created_at) AS day,
    language,
    count() AS events
FROM ghpulse.events
WHERE language != ''
GROUP BY day, language;

-- ─────────────────────────────────────────────────────────────────────────────────────────────
-- 3) activity_hourly_mv — активность по (день недели × час).
--
-- Источник для будущего GET /api/v1/activity/heatmap (2.4). Контракт в docs/ARCHITECTURE.md —
-- источнике истины по API — не перечисляет параметр окна для этого эндпоинта (в отличие от
-- /trending и /languages/trends, у которых `window` явно указан), а поле ответа называется
-- generic `events`, не `stars`/`pushes`. Отсюда два решения:
--   - без фильтра WHERE event_type — это тепловая карта ЛЮБОЙ активности, а не конкретного типа
--     события: имя поля ответа в контракте это подтверждает;
--   - агрегат "по всей истории", а не тайм-серия: группировка идёт по toDayOfWeek/toHour без даты,
--     поэтому у таблицы жёсткий потолок 7 × 24 = 168 строк вне зависимости от того, сколько событий
--     и сколько лет истории пройдёт через неё.
-- (Расхождение: черновик задачи 2.4 в TASKS_DETAILED.md содержит пример `?window=7d` для этого же
-- эндпоинта — это единственное место, где приватный планировочный документ разошёлся с публичным
-- docs/ARCHITECTURE.md. Если понадобится фильтр по окну, эта MV его не даст: она схлопывает дату
-- на вставке и не хранит день, только день недели. Стоит сверить и поправить один из двух документов
-- при выполнении 2.4, а не тянуть решение сюда явочным порядком.)
--
-- PARTITION BY намеренно отсутствует. PARTITION BY toYYYYMM у таблицы с потолком в 168 строк не
-- отсекает при чтении ничего полезного (сам объём меньше одной гранулы) и вместо этого гарантированно
-- размножает мелкие куски по партициям — ровно тот сценарий из 001_events.sql, которого там избегали
-- укрупнением партиции, но здесь избегают ещё жёстче: партиционирование не нужно вообще, весь корпус
-- умещается в единственный кусок после первого мержа.
--
-- ORDER BY (weekday, hour) — при 168 строках порядок не даёт измеримого выигрыша (эндпоинт читает
-- всю таблицу целиком, фильтров нет), выбран для читаемости результата и совпадения с порядком
-- вложенности в ответе контракта (`weekday` затем `hour`), а не по цене доступа.
--
-- toDayOfWeek(created_at) без явного mode — ClickHouse отдаёт 1=Monday…7=Sunday (ISO 8601).
-- Контрактный пример ответа начинает нумерацию с `"weekday":0` — числовая схема эндпоинта 2.4 ещё
-- не зафиксирована (0=Monday или 0=Sunday, как в JS Date.getDay()). Реализация 2.4 обязана явно
-- решить и обосновать перекодировку 1–7 → 0–6/0–6 на границе API, а не полагаться на то, что цифры
-- совпадут случайно.
CREATE MATERIALIZED VIEW IF NOT EXISTS ghpulse.activity_hourly_mv
(
    weekday UInt8,
    hour    UInt8,
    events  UInt64
)
ENGINE = SummingMergeTree
ORDER BY (weekday, hour)
AS
SELECT
    toDayOfWeek(created_at) AS weekday,
    toHour(created_at) AS hour,
    count() AS events
FROM ghpulse.events
GROUP BY weekday, hour;
